import mongoose from "mongoose";

//Creating the user schema
const userSchema = new mongoose.Schema(
  {
    username: {
      type: String,
      required: true,
      unique: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
    },
    password: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

//Creating the user model
const User = mongoose.model("User", userSchema);

//Exporting the user model
export default User;
